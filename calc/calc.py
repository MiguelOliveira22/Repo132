class Calculator():
    def __init__(self, valuea, valueb):
        self.a = valuea
        self.b = valueb

    def Soma(self):
        return self.a + self.b
    
    def Subt(self):
        return self.a - self.b
    
    def Mult(self):
        return self.a * self.b
    
    def Divi(self):
        return self.a / self.b